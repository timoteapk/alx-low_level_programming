#include <main.h>
/**
 * main -  prints lowercaase alphabets
 *
 * print_alphabet: prints functin lowercase alphabetes
 *
 * Return: always 0 successful
 */

void print_alphabet(void)
{
	for (char letter = 'a'; letter <= 'z'; letter++)
	{
		_putchar(letter);
	}
	_putchar('\n');
}

int main(void)
{
	void print_alphabet(void);
	return (0);
}
